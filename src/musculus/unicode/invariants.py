NONCHARACTER_CODE_POINTS = [
    range(0xFDD0, 0xFDF0),
    *[(x + 0xFFFE, x + 0xFFFF) for x in range(0x0, 0x110000, 0x10000)],
]

def is_noncharacter_code_point(cp: int | str, /) -> bool:
    cp = to_code_point(cp)
    return any(cp in r for r in NONCHARACTER_CODE_POINTS)


